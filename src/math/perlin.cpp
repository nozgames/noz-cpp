// Fade function: 6t^5 - 15t^4 + 10t^3
static inline float fade(float t) {
    return t * t * t * (t * (t * 6.0f - 15.0f) + 10.0f);
}

// Linear interpolation
static inline float lerp(float t, float a, float b) {
    return a + t * (b - a);
}

// Gradient function
static inline float grad(int hash, float x, float y) {
    int h = hash & 15;
    float u = h < 8 ? x : y;
    float v = h < 4 ? y : x;
    return ((h & 1) ? -u : u) + ((h & 2) ? -v : v);
}

float PerlinNoise(const Vec2& Position) {
    // Find unit grid cell containing point
    int X = (int)floorf(Position.x) & 255;
    int Y = (int)floorf(Position.y) & 255;

    // Get relative xy coordinates of point within cell
    float x = Position.x - floorf(Position.x);
    float y = Position.y - floorf(Position.y);

    // Compute fade curves for x and y
    float u = fade(x);
    float v = fade(y);

    // Hash coordinates of the 4 square corners
    int A = permutation[X] + Y;
    int AA = permutation[A];
    int AB = permutation[A + 1];
    int B = permutation[X + 1] + Y;
    int BA = permutation[B];
    int BB = permutation[B + 1];

    // Add blended results from 4 corners of square
    float result = lerp(v,
        lerp(u, grad(permutation[AA], x, y),
                grad(permutation[BA], x - 1.0f, y)),
        lerp(u, grad(permutation[AB], x, y - 1.0f),
                grad(permutation[BB], x - 1.0f, y - 1.0f)));

    // Return value in range [0, 1]
    return (result + 1.0f) * 0.5f;
}